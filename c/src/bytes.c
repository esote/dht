static uint16_t
bswap_16(uint16_t x)
{
	return (uint16_t)(x<<8 | x>>8);
}

static uint32_t
bswap_32(uint32_t x)
{
	return x>>24 | ((x>>8)&0xff00) | ((x<<8)&0xff0000) | x<<24;
}

static uint64_t
bswap_64(uint64_t x)
{
	return ((uint64_t)bswap_32((uint32_t)x))<<32 | bswap_32((uint32_t)(x>>32));
}

static uint16_t
hton_16b(uint16_t x)
{
	static const union { int i; char c; } u = { 1 };
	if (u.c == 1) {
		return bswap_16(x);
	}
	return x;
}

