void
hton_16(void *dst, uint16_t x)
{
	union { uint16_t i; uint8_t b[2]; } u;
	u.i = hton_16b(x);
	(void)memcpy(dst, u.b, 2);
}

uint16_t
ntoh_16(const void *src)
{
	union { uint8_t b[2]; uint16_t i; } u;
	(void)memcpy(u.b, src, 2);
	return ntoh_16b(u.i);
}

