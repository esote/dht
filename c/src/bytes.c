size_t
leading_zeros(const uint8_t *b, size_t len)
{
	size_t i;
	for (i = 0; i < len; i++) {
		if (b[i] != 0) {
			return i*8 + leading_zeros8(b[i]);
		}
	}
	return len*8;
}

size_t
lcp(const uint8_t *x, const uint8_t *y, size_t len)
{
	size_t i;
	uint8_t b;
	for (i = 0; i < len; i++) {
		if ((b = x[i] ^ y[i]) != 0) {
			return i*8 + leading_zeros8(b);
		}
	}
	return len*8;
}

void
memxor(uint8_t *dst, const uint8_t *a, const uint8_t *b, size_t len)
{
	size_t i;
	for (i = 0; i < len; i++) {
		dst[i] = a[i] ^ b[i];
	}
}

bool
is_zero(const uint8_t *src, size_t len)
{
	size_t i;
	for (i = 0; i < len; i++) {
		if (src[i] != 0) {
			return false;
		}
	}
	return true;
}
